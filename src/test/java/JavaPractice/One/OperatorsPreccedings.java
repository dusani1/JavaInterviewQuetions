package JavaPractice.One;

public class OperatorsPreccedings {
	public static void main(String[] args) {
		System.out.println(-6+4*2); //2
		System.out.println((2+3)*4); //20
		System.out.println(9+6-3*4/2%5); //14
		System.out.println(7+(3*5)*(4/2)+(5%3)-1);
		//7+15*2+2-1
		//7+30+2-1
		//38
	}
	
}
